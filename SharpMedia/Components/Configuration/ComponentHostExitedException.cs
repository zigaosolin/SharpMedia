using System;
using System.Collections.Generic;
using System.Text;

namespace SharpMedia.Components.Configuration
{
    /// <summary>
    /// An exception thrown by methods of a component when the host has already exited
    /// </summary>
    [global::System.Serializable]
    public class ComponentHostExitedException : Exception
    {
        //
        // For guidelines regarding the creation of new exception types, see
        //    http://msdn.microsoft.com/library/default.asp?url=/library/en-us/cpgenref/html/cpconerrorraisinghandlingguidelines.asp
        // and
        //    http://msdn.microsoft.com/library/default.asp?url=/library/en-us/dncscol/html/csharp07192001.asp
        //

        public ComponentHostExitedException() { }
        public ComponentHostExitedException(string message) : base(message) { }
        public ComponentHostExitedException(string message, Exception inner) : base(message, inner) { }
        protected ComponentHostExitedException(
          System.Runtime.Serialization.SerializationInfo info,
          System.Runtime.Serialization.StreamingContext context)
            : base(info, context) { }
    }
}
